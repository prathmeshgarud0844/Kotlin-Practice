import java.util.Scanner

fun main() {
    val scanner = Scanner(System.`in`)

    val line1 = scanner.nextLine()  // Read first line
    val line2 = scanner.nextLine()  // Read second line
    val num1 = scanner.nextInt()    // Read first number
    val num2 = scanner.nextInt()    // Read second number

    println(line1)
    println(line2)
    println(num1)
    println(num2)
}